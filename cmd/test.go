package main

import (
	"fmt"
	"regexp"
	"strings"
)

var externalIdRegexs = []*regexp.Regexp{
	// счёт(у)?(-оферт[ыа]?)? с возможным №, N, No, # и пробелами
	regexp.MustCompile(`(?i)(?:счёт[у]?(?:-оферт[ыа]?)?\s*)(?:№|N(?:o)?|#)?\s*(\d+)`),
	// сч(-оферт[ыа]?)? + №/N/No/# + цифры
	regexp.MustCompile(`(?i)(?:сч(?:-оферт[ыа]?)?\s*)(?:№|N(?:o)?|#)?\s*(\d+)`),
	// просто "счет" или "счёт" без оферты
	regexp.MustCompile(`(?i)(?:счет[ау]?\s*)(?:№|N(?:o)?|#)?\s*(\d+)`),
	// сч. (точка) + возможно оферта
	regexp.MustCompile(`(?i)сч\.(?:оферт[еы]?)?\s*(?:№|N(?:o)?|#)?\s*(\d+)`),
	// "согласно счета" и подобные (слово "счета" + цифры)
	regexp.MustCompile(`(?i)счета\s+(?:№|N(?:o)?|#)?\s*(\d+)`),
	// явное слово "оферта"/"оферты"/"оферте" + цифры
	regexp.MustCompile(`(?i)оферт[еыа]?\s*(?:№|N(?:o)?|#)?\s*(\d+)`),
	// слово "номер" + цифры (как в "номер 464373")
	regexp.MustCompile(`(?i)номер\s+(?:№|N(?:o)?|#)?\s*(\d+)`),
	// отдельно цифры после сокращений "сч/оферта" с дефисами и пробелами
	regexp.MustCompile(`(?i)(?:сч[- ]оферт[еыа]?)\s*(?:№|N(?:o)?|#)?\s*(\d+)`),
	// документ (с возможными падежами) + префикс и номер
	regexp.MustCompile(`(?i)документ[ауе]?\s*(?:№|N(?:o)?|#)?\s*(\d+)`),
	// платёж / платеж (с падежами) + префикс и номер
	regexp.MustCompile(`(?i)(?:платёж[ауе]?|платеж[ауе]?)\s*(?:№|N(?:o)?|#)?\s*(\d+)`),
}

func main() {
	desc := "За подарочные сертификаты по сч №432170 от 02.03.26 г. Без НДС"
	for i, regex := range externalIdRegexs {
		submatch := regex.FindStringSubmatch(desc)
		fmt.Println(i, ": ", strings.Join(submatch, ","))
	}
}
